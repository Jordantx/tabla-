    public Tablas() {
        initComponents();
        
        String[] titulo=new String[]{"Id","Nombre","Apelido"};
        dtm.setColumnIdentifiers(titulo); //ponerlo de cabesera en la tabla
        tbldatos.setModel(dtm);
    }
    void agregar(){
        dtm.addRow(new Object[]{ //metodo addRow anadir contenido de la fila 
           txtId.getText(),txtNombre.getText(),txtApellido.getText() //poner lo valores 
         });
    }
    void eliminar(){
        int fila=tbldatos.getSelectedRow(); //metodo queseleccionala fila  
        
        if (fila >= 0) {
        // Eliminar la fila del modelo de la tabla
        dtm.removeRow(fila);
    } else {
        // Mostrar un mensaje si no hay ninguna fila seleccionada
        JOptionPane.showMessageDialog(null, "Por favor, selecciona una fila para eliminar.");
    }
         
    }
    
    void actualizar(){
        int fila=tbldatos.getSelectedRow(); 
        if (fila >= 0) {
        dtm.setValueAt(txtId.getText(), fila, 0);
        dtm.setValueAt(txtNombre.getText(), fila, 1);
        dtm.setValueAt(txtApellido.getText(), fila, 2);
        }else{
            JOptionPane.showMessageDialog(null, "falta completar");
        }
    }
    void limpiar(){
        int filas =dtm.getColumnCount();//sabemos cuantas filar tenemos
        for(int i=0;i<filas;i++){
            dtm.removeRow(0);
        } 
        
    
    
    }
    
    
    
    
    
   
